use std::path::PathBuf;
use std::process::Command;

use tauri::AppHandle;

use super::{VirtualDeviceConfig, VirtualDriverStatus};

const CONF_NAME: &str = "50-splitwave-sinks.conf";
const NODE_PREFIX: &str = "splitwave";

fn conf_path() -> Option<PathBuf> {
    Some(dirs::config_dir()?.join("pipewire/pipewire.conf.d").join(CONF_NAME))
}

// node.name is the stable handle, node.description is the label shown in
// system settings. Quotes would break both the .conf and the pactl arg, so
// drop them from the label.
fn clean_label(name: &str) -> String {
    name.replace(['"', '\''], "")
}

pub fn status() -> VirtualDriverStatus {
    let ok = Command::new("pactl")
        .arg("info")
        .output()
        .map(|o| o.status.success())
        .unwrap_or(false);
    VirtualDriverStatus { installed: ok }
}

pub fn install(_app: &AppHandle) -> Result<(), String> {
    // No driver to install on PipeWire; sinks are created directly.
    Ok(())
}

pub fn uninstall() -> Result<(), String> {
    unload_runtime_sinks();
    if let Some(p) = conf_path() {
        let _ = std::fs::remove_file(p);
    }
    Ok(())
}

pub fn apply_virtual_devices(devices: Vec<VirtualDeviceConfig>) -> Result<(), String> {
    unload_runtime_sinks();

    let conf = conf_path().ok_or("no config directory")?;
    if devices.is_empty() {
        let _ = std::fs::remove_file(&conf);
        return Ok(());
    }

    if let Some(parent) = conf.parent() {
        std::fs::create_dir_all(parent).map_err(|e| format!("create conf dir: {e}"))?;
    }
    std::fs::write(&conf, conf_contents(&devices)).map_err(|e| format!("write conf: {e}"))?;

    for d in &devices {
        create_runtime_sink(&d.id, &clean_label(&d.name))?;
    }
    Ok(())
}

fn conf_contents(devices: &[VirtualDeviceConfig]) -> String {
    let mut out = String::from("# Auto-generated by Splitwave. Do not edit.\n\ncontext.objects = [\n");
    for d in devices {
        let desc = clean_label(&d.name);
        out.push_str("  {\n");
        out.push_str("    factory = adapter\n");
        out.push_str("    args = {\n");
        out.push_str("      factory.name     = support.null-audio-sink\n");
        out.push_str(&format!("      node.name        = \"{NODE_PREFIX}.{}\"\n", d.id));
        out.push_str(&format!("      node.description = \"{desc}\"\n"));
        out.push_str("      media.class      = Audio/Sink\n");
        out.push_str("      audio.position   = [ FL FR ]\n");
        out.push_str("      object.linger    = true\n");
        out.push_str("    }\n");
        out.push_str("  }\n");
    }
    out.push_str("]\n");
    out
}

fn create_runtime_sink(id: &str, label: &str) -> Result<(), String> {
    let out = Command::new("pactl")
        .args([
            "load-module",
            "module-null-sink",
            &format!("sink_name={NODE_PREFIX}.{id}"),
            &format!("sink_properties=\"node.description='{label}'\""),
        ])
        .output()
        .map_err(|e| format!("pactl load-module: {e}"))?;
    if !out.status.success() {
        return Err(format!(
            "pactl load-module failed: {}",
            String::from_utf8_lossy(&out.stderr).trim()
        ));
    }
    Ok(())
}

fn unload_runtime_sinks() {
    let Ok(out) = Command::new("pactl").args(["list", "short", "modules"]).output() else {
        return;
    };
    let text = String::from_utf8_lossy(&out.stdout);
    let prefix = format!("sink_name={NODE_PREFIX}.");
    for line in text.lines() {
        let mut cols = line.split('\t');
        let (Some(id), Some(kind), Some(args)) = (cols.next(), cols.next(), cols.next()) else {
            continue;
        };
        if kind == "module-null-sink" && args.contains(&prefix) {
            let _ = Command::new("pactl").args(["unload-module", id]).status();
        }
    }
}
